class Vehicle {
  final String licensePlate;
  final String model;
  final String make;
  final String type;
  final String status;
  final int year;
  final int sittingCapacity;

  const Vehicle({
    required this.licensePlate,
    required this.model,
    required this.make,
    required this.type,
    required this.status,
    required this.year,
    required this.sittingCapacity,
  });
}
